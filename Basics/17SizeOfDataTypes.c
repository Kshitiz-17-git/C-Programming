//Program to find the size of the data types in bytes using sizeof operator
#include <stdio.h>

int main() {
    //Printing the size of the different data types
    printf("Size of Integer DataType is %d bytes\n", sizeof(int));
    printf("Size of Character DataType is %d bytes\n", sizeof(char));
    printf("Size of Float DataType is %d bytes\n", sizeof(float));
    printf("Size of Double DataType is %d bytes\n", sizeof(double));

return 0;
}